function update(lang, keys) {
  const filePath = path.join(__dirname, 'src', 'i18n', 'locales', `${lang}.json`);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  fs.writeFileSync(filePath, JSON.stringify({ ...data, ...keys }, null, 2));
}

